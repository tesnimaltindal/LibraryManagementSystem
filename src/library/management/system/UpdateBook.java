    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
           if(txtBookID.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "Please Enter Book ID for Update!");
        txtBookID.requestFocus();
    } else {
        try {
            pst = con.prepareStatement("SELECT * FROM BOOK WHERE ID=?");
            pst.setString(1, txtBookID.getText());
            
            rs = pst.executeQuery();  // Assign the result to the ResultSet

            if(rs.next()) {
                txtName.setText(rs.getString("nameofbook"));
                txtAuthor.setText(rs.getString("author"));
                thtPublisher.setText(rs.getString("publisher"));
                txtPrice.setText(rs.getString("price"));
                txtYear.setText(rs.getString("yearPublished"));
            } else {
                JOptionPane.showMessageDialog(this, "Please Enter Valid Book ID"); 
            }
        } catch (SQLException ex) {
            Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
        }           
    }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void txtUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtUpdateActionPerformed
        // TODO add your handling code here:
        try
        {
            String bookID=txtBookID.getText();
            String bookName= txtName.getText();
            String author = txtAuthor.getText();
            String publisher=thtPublisher.getText();
            String price = txtPrice.getText();
            String year = txtYear.getText();
            
            //sql update 
            pst = con.prepareStatement("UPDATE book SET nameofbook=?,author=?,publisher=?,price=? ,yearpublished=? WHERE id=?");
            pst.setString(1, bookName);
            pst.setString(2, author);
            pst.setString(3, publisher);
            pst.setString(4, price);
            pst.setString(5, year);
            pst.setString(6, bookID);
            int rowsUpdated=pst.executeUpdate();
            if(rowsUpdated>0)
            {
                 JOptionPane.showMessageDialog(this, "Book Information Updated Successfully");
                    
                    // Clear the text fields
                    txtBookID.setText("");
                    txtName.setText("");
                    txtAuthor.setText("");
                    thtPublisher.setText("");
                    txtPrice.setText("");
                    txtYear.setText("");
            }
            
                   else {
                    JOptionPane.showMessageDialog(this, "No matching record found. Please check the Book ID.");
                }
            } catch (SQLException ex) {
                Logger.getLogger(UpdateBook.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_txtUpdateActionPerformed

