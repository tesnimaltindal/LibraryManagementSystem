    private void btnReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnActionPerformed
        // TODO add your handling code here:
        if(txtStudentID.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Please Enter Student ID ");
            txtStudentID.requestFocus();
            
        }
        else
        {
          
           if (txtBookID.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Book ID is required.");
            txtBookID.requestFocus();
    }    else {
     try {
        Connection con = Connect.ConnecttoDB();
        PreparedStatement pst = con.prepareStatement("UPDATE book SET statusofbook = ?, issuedate = ?, duedate = ?, studentid = ? WHERE id=? AND studentid = ?");
        
        // Setting the values for the parameters
        pst.setString(1, "NOT-ISSUED");
        pst.setNull(2, java.sql.Types.DATE); 
        pst.setNull(3, java.sql.Types.DATE); 
        pst.setNull(4, java.sql.Types.VARCHAR); 
        pst.setString(5, txtBookID.getText());
        pst.setString(6,txtStudentID.getText());
        
        int rowsUpdated = pst.executeUpdate();
        if(rowsUpdated>0)
        {
            
        JOptionPane.showMessageDialog(this, "Book Returned.");
        
        // Clearing the text fields
        txtIssueaDate.setText("");
        txtBookID.setText("");
        txtBookName.setText("");
        txtStudentID.setText("");
        txtStudentName.setText("");
        txtDueDate.setText("");
        }
        else {
                    JOptionPane.showMessageDialog(this, "No matching record found. Please check the Book ID and Student ID.");
        } 
     }catch (SQLException ex) {
                Logger.getLogger(AddBook.class.getName()).log(Level.SEVERE, null, ex);
            }
}

          
            
            
            
        }
    }//GEN-LAST:event_btnReturnActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if(txtStudentID.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Please Enter Student ID ");
            txtStudentID.requestFocus();
            
        }
        else
        {
            try{
                
                pst =con.prepareStatement("SELECT student.id AS student_id, student.name AS student_name, book.id AS book_id,  book.nameofbook, book.issuedate, book.duedate FROM book INNER JOIN student ON book.studentid = student.id where student.id=?");
                pst.setString(1,txtStudentID.getText());
               
                rs=pst.executeQuery();
                  if(rs.next())
                  {
                     txtBookID.setText(rs.getString(3));
                     txtStudentName.setText(rs.getString(2));
                     txtBookName.setText(rs.getString(4));
                     txtIssueaDate.setText(rs.getString(5));
                     txtDueDate.setText(rs.getString(6));
                  
                  }
                  else
                  {
                      JOptionPane.showMessageDialog(this,"Student Has No Book ");
                  }
            }
           catch (SQLException ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
        }           
                
                 
           
        }
    }//GEN-LAST:event_btnSearchActionPerformed

