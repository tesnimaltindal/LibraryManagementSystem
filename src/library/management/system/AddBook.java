    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(txtID.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"All Field is Required");
            txtID.requestFocus();
        }
        else if(txtName.getText().equals(""))
        {
            
            JOptionPane.showMessageDialog(this,"All Field is Required");
            txtName.requestFocus();
        }
         else if(txtAuthor.getText().equals(""))
        {
            
            JOptionPane.showMessageDialog(this,"All Field is Required");
            txtAuthor.requestFocus();
        }
         else if(txtPublisher.getText().equals(""))
        {
            
            JOptionPane.showMessageDialog(this,"All Field is Required");
            txtPublisher.requestFocus();
        }
         else if(txtPrice.getText().equals(""))
        {
            
            JOptionPane.showMessageDialog(this,"All Field is Required");
            txtPrice.requestFocus();
        }
         else if(txtYear.getText().equals(""))
        {
            
            JOptionPane.showMessageDialog(this,"All Field is Required");
            txtYear.requestFocus();
        }

        else
          {
              try
              {
              Connection con = Connect.ConnecttoDB();
              PreparedStatement pst=con.prepareStatement("INSERT INTO BOOK (id,nameofbook,author,publisher,price,yearpublished,statusofbook)values (?,?,?,?,?,?,?)");
              pst.setString(1,txtID.getText());
              pst.setString(2,txtName.getText());
              pst.setString(3,txtAuthor.getText());
              pst.setString(4,txtPublisher.getText());
              pst.setString(5,txtPrice.getText());
              pst.setString(6,txtYear.getText());
              pst.setString(7,"NOT-ISSUED");
              pst.executeUpdate();
              JOptionPane.showMessageDialog(this,"Record Saved.");
              txtID.setText("");
              txtName.setText("");
              txtAuthor.setText("");
              txtPublisher.setText("");
              txtPrice.setText("");
              txtYear.setText("");
              }
            catch(SQLException ex) {
             Logger.getLogger(AddBook.class.getName()).log(Level.SEVERE, null, ex);
              }
         }
        
    }//GEN-LAST:event_btnSaveActionPerformed

