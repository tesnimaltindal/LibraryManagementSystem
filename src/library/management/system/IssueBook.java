    private void btnIssueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIssueActionPerformed
        // TODO add your handling code here:
        if (txtBookID.getText().equals("")) {
    JOptionPane.showMessageDialog(this, "Book ID is required.");
    txtBookID.requestFocus();
} else if (txtStdentID.getText().equals("")) {
    JOptionPane.showMessageDialog(this, "Student ID is required.");
    txtStdentID.requestFocus();
} else {
    try {
        Connection con = Connect.ConnecttoDB();
        
        // Kitabın mevcut durumunu kontrol eden sorgu
        PreparedStatement checkStatus = con.prepareStatement("SELECT statusofbook FROM book WHERE id = ?");
        checkStatus.setString(1, txtBookID.getText());
        ResultSet rs = checkStatus.executeQuery();
        
        if (rs.next()) {
            String status = rs.getString("statusofbook");
            
            if ("ISSUED".equals(status)) {
                JOptionPane.showMessageDialog(this, "This book is already issued to another student.");
            } else {
                // Kitabı güncelleyip ödünç verme işlemi
                PreparedStatement pst = con.prepareStatement("UPDATE book SET statusofbook = ?, issuedate = ?, duedate = ?, studentid = ? WHERE id = ?");
                pst.setString(1, "ISSUED");
                pst.setString(2, txtIssueDate.getText());
                pst.setString(3, txtDueDate.getText());
                pst.setString(4, txtStdentID.getText());
                pst.setString(5, txtBookID.getText());
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Book Issued.");
                
                // Metin alanlarını temizle
                txtIssueDate.setText("");
                txtBookID.setText("");
                txtBookName.setText("");
                txtStdentID.setText("");
                txtDueDate.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Book not found.");
        }
    } catch (SQLException ex) {
        Logger.getLogger(AddBook.class.getName()).log(Level.SEVERE, null, ex);
    }
}

       
    }//GEN-LAST:event_btnIssueActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        
        if(txtBookID.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "Please Enter Book ID for search!");
        txtBookID.requestFocus();
    } else {
        try {
            pst = con.prepareStatement("SELECT * FROM BOOK WHERE ID=?");
            pst.setString(1, txtBookID.getText());
            rs = pst.executeQuery();  // Assign the result to the ResultSet

            if(rs.next()) {
                txtBookName.setText(rs.getString("nameofbook"));
            } else {
                JOptionPane.showMessageDialog(this, "Please Enter Valid Book ID"); 
            }
        } catch (SQLException ex) {
            Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
        }           
    }
                
                
    }//GEN-LAST:event_btnSearchActionPerformed

